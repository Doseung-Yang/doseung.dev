export function pickCommentsById(id: string, count = 2): MockComment[] {
  if (count >= MOCK_COMMENTS.length) return MOCK_COMMENTS.slice(0, count);
  const sum = Array.from(id).reduce((acc, ch) => acc + ch.charCodeAt(0), 0);
  const start = sum % MOCK_COMMENTS.length;
  const res: MockComment[] = [];
  for (let i = 0; i < count; i++) {
    res.push(MOCK_COMMENTS[(start + i) % MOCK_COMMENTS.length]);
  }
  return res;
}
